package org.codewithme.store.dtos;

public class UpdateUserRequest {
    public String name;
    public String email;

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }
}
